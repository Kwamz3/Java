public class literals {
    public static void main(String a [])
    {
        int num1 = 0x7E;
        System.out.println(num1);

        long num2 = 10_000_000_00;
        System.out.println(num2);
        
        double num3 = 12e11;
        System.out.println(num3);

        char c = '2';
        c++;
        System.out.println(c);
    }
}
